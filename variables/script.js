"use-stric";

//Use "let variableName;" to declare a variable named "variableName"
let admin;
let name;

//Value assignment to a variable
name = "John";
admin = name;

//Example to use a variable
alert(admin);

//Note you can assign a value directly in the declaration line
let intValue = 1;

/*
Use "const constantName;" to declare a constant named "constantName".
The value of a constant cannot be modified once initialized.
Uppercase the constant name when the value is set in the source code.
Otherwise, the constant name is lowercase.
*/
const BIRTHDAY = '12.24.1982';
const age = age(BIRTHDAY);

